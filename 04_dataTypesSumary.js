/**
 * NOTE: JavaScript number of data types
 * ---------------------------------------
 * Javascript has two type of data type
 * (1). Primitive data types
 * In primitive data type javascript offers seven data types.
 * _(1). Number
 * Number used for all number values (Integer and floating point) except for very big integers.
 * _(2). Bigint
 * Bigint used for arbitrarily large integers.
 * _(3). String
 * String used to store text.
 * _(4). Boolean
 * Boolean used for conditional logic (True or False).
 * _(5). Symbol
 * Symbol used for creating unique identifiers that won't collide.
 * _(6). Undefine
 * Undefine indicating that a variable has not been assigned a value.
 * _(7). Null
 * Null indicating a deliberate non-value.
 */

